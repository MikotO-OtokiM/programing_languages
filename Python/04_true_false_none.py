# False と判定される例
# リストなど要素があるかどうかの判定で利用できる
# 0, 0.0, '', [], (), {}, set()


################################################################################
# == と is の違い
# True となる（0 以外の数値は True と判定されるため）
print(1 == True)

# False となる（is はオブジェクトそのものが同じかどうかを判定しているため）
print(1 is True)

# is の利用例
# None は空のオブジェクトを表す
is_empty = None

# そのオブジェクトが None かどうか判定する場合
if is_empty is None:
    print('None')