# True と判定される例
# True が代入された変数
is_ok = True
if is_ok:
    print('OK')
else:
    print('NG')

# 0 でない数値（正または負の数値は True、0 は False になる）
is_ok = 1
if is_ok:
    print('OK')
else:
    print('NG')

# 空でないリスト（空のリスト [] の場合は False になる）
is_ok = [1, 2, 3]
if is_ok:
    print('OK')
else:
    print('NG')

# in と not in
y = [1, 2, 3]
x = 1
 # y に x が含まれているか
if x in y:
    print('in')

# y に 100 が含まれていないか
if 100 not in y:
    print('not in')


